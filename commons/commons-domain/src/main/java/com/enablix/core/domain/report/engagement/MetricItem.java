package com.enablix.core.domain.report.engagement;

public class MetricItem {

	private String label;
	
	private long count;

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public long getCount() {
		return count;
	}

	public void setCount(long count) {
		this.count = count;
	}
	
}
